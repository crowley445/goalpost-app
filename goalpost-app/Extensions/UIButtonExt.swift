import UIKit

extension UIButton {
    
    func setSelectedColor () {
        backgroundColor = #colorLiteral(red: 0.3760718405, green: 0.794663012, blue: 0.3411160707, alpha: 1)
    }
    
    func setDeselectedColor() {
        backgroundColor = #colorLiteral(red: 0.3760718405, green: 0.794663012, blue: 0.3411160707, alpha: 0.5)
    }

}
